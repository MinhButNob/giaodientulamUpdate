    @Override
    public void open() {
        this.setLocationRelativeTo(null);
        this.selectTimeRange();
        this.fillBills();
    }

    @Override
    public void fillBills() {
        Date begin = XDate.parse(txtBegin.getText());
        Date end = XDate.parse(txtEnd.getText());
        bills = billDao.findByUserAndTimeRange(XAuth.user.getUsername(), begin, end);

        DefaultTableModel model = (DefaultTableModel) tblBills.getModel();
        model.setRowCount(0);
        String[] statuses = {"Servicing", "Completed", "Canceled"};
        bills.forEach(b -> {
            String checkout = XDate.format(b.getCheckout(), "HH:mm:ss dd-MM-yyyy");
            Object[] row = {
                b.getId(),
                "Card #" + b.getCardId(),
                XDate.format(b.getCheckin(), "HH:mm:ss dd-MM-yyyy"),
                checkout,
                statuses[b.getStatus()]
            };
            model.addRow(row);
        });
    }

    @Override
    public void selectTimeRange() {
        TimeRange range = TimeRange.today();
        switch (cboTimeRanges.getSelectedIndex()) {
            case 0 ->
                range = TimeRange.today();
            case 1 ->
                range = TimeRange.thisWeek();
            case 2 ->
                range = TimeRange.thisMonth();
            case 3 ->
                range = TimeRange.thisQuarter();
            case 4 ->
                range = TimeRange.thisYear();
        }
        Date begin = java.sql.Date.valueOf(range.getBegin());
        Date end = java.sql.Date.valueOf(range.getEnd());
        Date endDate = Date.from(range.getEnd().atStartOfDay(ZoneId.systemDefault()).toInstant());
txtEnd.setText(XDate.format(endDate));
        Date beginDate = Date.from(range.getBegin().atStartOfDay(ZoneId.systemDefault()).toInstant());
txtBegin.setText(XDate.format(beginDate));
        this.fillBills();
    }

    @Override
    public void showBillJDialog() {
        Bill bill = bills.get(tblBills.getSelectedRow());

        DrinkJDialog dialog = new DrinkJDialog((Frame) this.getOwner(), true);
        dialog.setBill(bill);
        dialog.setVisible(true);

        dialog.addWindowListener(new java.awt.event.WindowAdapter() {
            @Override
            public void windowClosed(java.awt.event.WindowEvent e) {
                History.this.fillBills();
            }
        });
    }
