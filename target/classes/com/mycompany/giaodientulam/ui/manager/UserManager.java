    @Override
    public void setForm(User entity) {
    panel.setName(entity.getUsername());
    txtPassword.setText(entity.getPassword());
    txtConfirm.setText(entity.getPassword());
    txtFullname.setText(entity.getFullname());
    pnlPhoto.setText(entity.getPhoto());

    // Xác định trạng thái hoạt động
    rdoHoatDong.setSelected(entity.isEnabled());
    rdoKhongHoatDong.setSelected(!entity.isEnabled());

    // Xác định vai trò của người dùng
    rdoQuanLy.setSelected(entity.isManager());
    rdoNhanVien.setSelected(!entity.isManager());
    }
@Override
public User getForm() {
    User entity = new User();
    entity.setUsername(txtUsername.getText().trim());
    entity.setPassword(new String(txtPassword.getPassword()));
    entity.setFullname(txtFullname.getText().trim());
    entity.setPhoto(pnlPhoto.getText()); // Nếu pnlPhoto là JTextField, nếu không cần sửa lại

    entity.setEnabled(rdoHoatDong.isSelected());
    entity.setManager(rdoQuanLy.isSelected());

    return entity;
}


    @Override
    public void fillToTable() {
         DefaultTableModel model = (DefaultTableModel) tblUsers.getModel();
        model.setRowCount(0);

        items = dao.findAll();
        items.forEach(item -> {
            Object[] rowData = {
                item.getUsername(),
                item.getPassword(),
                item.getFullname(),
                item.getPhoto(),
                item.isManager() ? "Quản lý" : "Nhân viên",
                item.isEnabled() ? "Hoạt động" : "Tạm dừng",
                false
            };
            model.addRow(rowData);
        });
    }


    @Override
    public void edit() {
        User entity = items.get(tblUsers.getSelectedRow());
        this.setForm(entity);
        this.setEditable(true);
        tabs.setSelectedIndex(1);
    }


    @Override
    public void create() {
        User entity = this.getForm();
        dao.create(entity);
        this.fillToTable();
        this.clear();
    }

    @Override
    public void update() {
        User entity = this.getForm();
        dao.update(entity);
        this.fillToTable();
    }

    @Override
    public void delete() {
        if (XDialog.confirm("Bạn thực sự muốn xóa?")) {
            String id = panel.getName();
            dao.deleteById(id);
            this.fillToTable();
            this.clear();
        }
    }
    @Override
    public void clear() {
        this.setForm(new User());
        this.setEditable(false);
    }

    @Override
    public void setEditable(boolean editable) {
           panel.setEnabled(!editable);
        btnCreate.setEnabled(!editable);
        btnUpdate.setEnabled(editable);
        btnDelete.setEnabled(editable);

        int rowCount = tblUsers.getRowCount();
        btnMoveFirst.setEnabled(editable && rowCount > 0);
        btnMovePrevious.setEnabled(editable && rowCount > 0);
        btnMoveNext.setEnabled(editable && rowCount > 0);
        btnMoveLast.setEnabled(editable && rowCount > 0);
    }


